  /**
   * Test process status
   */
  async testProcessStatus() {
    const test = {
      name: 'Process Status',
      category: 'system',
      startTime: Date.now()
    };

    try {
      const { stdout } = await execAsync('pm2 jlist');
      // PM2 sometimes outputs non-JSON messages before the JSON array
      // e.g., ">>>> In-memory PM2 is started" - extract the JSON array
      let jsonStr = stdout;
      const jsonStart = stdout.indexOf('[');
      if (jsonStart > 0) {
        jsonStr = stdout.slice(jsonStart);
      }
      const processes = JSON.parse(jsonStr);
      const lanAgent = processes.find(p => p.name === 'lan-agent');
      
      if (lanAgent) {
        test.status = lanAgent.pm2_env.status === 'online' ? 'passed' : 'warning';
        test.details = {
          status: lanAgent.pm2_env.status,
          restarts: lanAgent.pm2_env.restart_time,
          uptime: Math.floor((Date.now() - lanAgent.pm2_env.created_at) / 1000 / 60) + ' minutes',
          memory: Math.floor(lanAgent.monit.memory / 1024 / 1024) + ' MB',
          cpu: lanAgent.monit.cpu + '%'
        };
      } else {
        test.status = 'failed';
        test.details = { error: 'LANAgent process not found' };
      }
    } catch (error) {
      test.status = 'failed';
      test.details = { error: error.message };
    }

    test.duration = Date.now() - test.startTime;
    return test;
  }

  /**
   * Collect system information
   */
  async collectSystemInfo() {
    try {
      const [mem, disks, cpu, network, docker] = await Promise.all([
        si.mem(),
        si.fsSize(),
        si.cpu(),
        si.networkInterfaces(),
        si.dockerInfo().catch(() => null)
      ]);

      return {
        memoryUsage: {
          total: mem.total,
          used: mem.used,
          percentage: (mem.used / mem.total) * 100
        },
        diskUsage: disks.map(disk => ({
          filesystem: disk.fs,
          size: disk.size,
          used: disk.used,
          available: disk.available,
          percentage: disk.use,
          mount: disk.mount
        })),
        cpu: {
          model: cpu.brand,
          cores: cpu.cores,
          speed: cpu.speed
        },
        network: network.filter(n => !n.internal).map(n => ({
          interface: n.iface,
          ip4: n.ip4,
          mac: n.mac
        })),
        docker: docker ? {
          running: docker.containers.running,
          paused: docker.containers.paused,
          stopped: docker.containers.stopped
        } : null,
        uptime: os.uptime(),
        loadAverage: os.loadavg(),
        nodeVersion: process.version,
        platform: process.platform,
        hostname: os.hostname()
      };
    } catch (error) {
      logger.error('Failed to collect system info:', error, { service: 'diagnostics' });
      return {};
    }
  }

