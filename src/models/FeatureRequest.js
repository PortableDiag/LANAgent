FeatureRequestSchema.statics.getByPriority = function(priority) {
  const cacheKey = `getByPriority-${priority}`;
  const cachedData = featureRequestCache.get(cacheKey);
  if (cachedData) return Promise.resolve(cachedData);
  return retryOperation(() => this.find({ priority }).sort({ submittedAt: -1 }))
    .then(data => { featureRequestCache.set(cacheKey, data); return data; })
    .catch(err => {
      logger.error(`Error fetching feature requests by priority: ${err.message}`);
      throw err;
    });
};

