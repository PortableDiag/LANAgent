  const cacheKey = `getByCategory-${category}`;
  const cachedData = featureRequestCache.get(cacheKey);
  if (cachedData) return Promise.resolve(cachedData);
  return retryOperation(() => this.find({ category }).sort({ priority: -1, submittedAt: -1 }))
    .then(data => { featureRequestCache.set(cacheKey, data); return data; })
