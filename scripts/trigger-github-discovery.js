#!/usr/bin/env node
import dotenv from 'dotenv';
import path from 'path';
import { fileURLToPath } from 'url';
import mongoose from 'mongoose';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

dotenv.config({ path: path.join(__dirname, '../.env') });

// Import services
import { GitHubFeatureDiscovery } from '../src/services/githubFeatureDiscovery.js';
import { FeatureRequest } from '../src/models/FeatureRequest.js';
import { logger } from '../src/utils/logger.js';

async function triggerDiscovery() {
  console.log('🚀 Triggering GitHub Feature Discovery...\n');
  
  try {
    // Connect to MongoDB
    await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/lanagent');
    console.log('✅ Connected to MongoDB');
    
    // Create discovery instance
    const discovery = new GitHubFeatureDiscovery({ config: { name: 'LANAgent' } });
    
    // Run discovery
    console.log('🔍 Discovering features from GitHub...\n');
    const features = await discovery.discoverFeaturesFromGitHub();
    
    console.log(`✨ Found ${features.length} features!\n`);
    
    // Show features with GitHub references
    let featuresWithRefs = 0;
    let featuresWithCode = 0;
    
    for (const feature of features) {
      if (feature.githubReference) {
        featuresWithRefs++;
        if (feature.githubReference.codeSnippet) {
          featuresWithCode++;
        }
      }
    }
    
    console.log(`📊 Statistics:`);
    console.log(`   Total features: ${features.length}`);
    console.log(`   Features with GitHub references: ${featuresWithRefs}`);
    console.log(`   Features with code snippets: ${featuresWithCode}`);
    
    // Show top 5 features with code
    console.log('\n📌 Top features with code snippets:\n');
    const withCode = features.filter(f => f.githubReference && f.githubReference.codeSnippet);
    
    for (const feature of withCode.slice(0, 5)) {
      console.log(`Feature: ${feature.description}`);
      console.log(`Source: ${feature.source}`);
      console.log(`File: ${feature.githubReference.filePath}`);
      console.log(`Code lines: ${feature.githubReference.codeSnippet.split('\n').length}`);
      console.log(`---`);
    }
    
    // Create feature requests for top features with GitHub references
    console.log('\n💾 Creating feature requests for top discoveries...\n');
    // Get features with GitHub references, prioritized by confidence
    const withGitHubRef = features.filter(f => f.githubReference)
      .sort((a, b) => {
        const confidenceOrder = { 'high': 3, 'medium': 2, 'low': 1 };
        return (confidenceOrder[b.confidence] || 0) - (confidenceOrder[a.confidence] || 0);
      })
      .slice(0, 5); // Store top 5 features
    
    for (const feature of withGitHubRef) {
      try {
        const existing = await FeatureRequest.findOne({
          title: { $regex: feature.description.substring(0, 50), $options: 'i' }
        });
        
        if (!existing) {
          const featureData = {
            title: feature.description,
            description: `Auto-discovered from GitHub: ${feature.description}\n\nSource: ${feature.sourceUrl}`,
            category: feature.type === 'plugin_idea' ? 'plugin-new' : 'core',
            priority: feature.confidence === 'high' ? 'high' : 'medium',
            status: 'submitted',
            autoGenerated: true,
            submittedBy: 'github_discovery',
            githubReferences: feature.githubReference ? [feature.githubReference] : [],
            implementationExamples: feature.implementationExample ? [feature.implementationExample] : []
          };
          
          const created = await FeatureRequest.create(featureData);
          console.log(`✅ Created feature request: ${created.title.substring(0, 50)}...`);
        } else {
          console.log(`⏭️  Feature already exists: ${feature.description.substring(0, 50)}...`);
        }
      } catch (error) {
        console.error(`❌ Failed to create feature request: ${error.message}`);
      }
    }
    
    console.log('\n✅ GitHub discovery completed!');
    
  } catch (error) {
    console.error('❌ Error during discovery:', error);
  } finally {
    await mongoose.disconnect();
  }
}

triggerDiscovery().catch(console.error);