#!/usr/bin/env node

import mongoose from 'mongoose';
import { FeatureRequest } from '../src/models/FeatureRequest.js';
import { DiscoveredFeature } from '../src/models/DiscoveredFeature.js';

async function migrateDiscoveredFeatures() {
  try {
    // Connect to MongoDB - use lanagent database where features are actually stored
    await mongoose.connect('mongodb://localhost:27017/lanagent');
    console.log('Connected to lanagent database');

    // First, let's see what's in the FeatureRequest collection
    const allFeatures = await FeatureRequest.find({});
    console.log(`Total features in FeatureRequest collection: ${allFeatures.length}`);
    
    if (allFeatures.length > 0) {
      console.log('\nSample features:');
      allFeatures.slice(0, 3).forEach(f => {
        console.log(`  - ${f.title}`);
        console.log(`    submittedBy: ${f.submittedBy}`);
        console.log(`    autoGenerated: ${f.autoGenerated}`);
        console.log(`    tags: ${f.tags}`);
      });
    }
    
    // Find ONLY GitHub-discovered features (not self-modification features)
    const githubFeatures = await FeatureRequest.find({
      submittedBy: { $in: ['github_discovery', 'github_discovery_scheduler'] }
    });

    console.log(`Found ${githubFeatures.length} GitHub-discovered features to migrate`);

    let migrated = 0;
    let errors = 0;

    for (const feature of githubFeatures) {
      try {
        // Create new DiscoveredFeature
        const discoveredFeature = new DiscoveredFeature({
          title: feature.title,
          description: feature.description,
          type: 'readme_feature', // Use a valid type
          source: {
            repository: 'github_migration', // Will be updated by next discovery run
            url: feature.githubReferences?.[0]?.url || 'https://github.com',
            filePath: feature.githubReferences?.[0]?.filePath,
            language: feature.githubReferences?.[0]?.language
          },
          implementation: {
            suggestion: feature.implementation || '',
            confidence: 'medium',
            targetFile: feature.implementationFile,
            estimatedEffort: 'medium' // Map to valid values: small, medium, large
          },
          status: feature.status === 'completed' ? 'implemented' : 'discovered',
          discoveredBy: feature.submittedBy,
          tags: Array.isArray(feature.tags) ? feature.tags : ['migrated'],
          createdAt: feature.createdAt || new Date(),
          updatedAt: feature.updatedAt || new Date()
        });

        // Add code snippets if available
        if (feature.githubReferences && feature.githubReferences.length > 0) {
          discoveredFeature.codeSnippets = feature.githubReferences.map(ref => ({
            code: ref.codeSnippet ? ref.codeSnippet.substring(0, 10000) : '',
            language: ref.language || 'javascript',
            filePath: ref.filePath,
            contextNotes: ref.contextNotes
          }));
        }

        // Save the discovered feature
        await discoveredFeature.save();
        migrated++;

        // Delete the original feature request
        await FeatureRequest.deleteOne({ _id: feature._id });
        console.log(`Migrated: ${feature.title}`);

      } catch (error) {
        console.error(`Failed to migrate feature "${feature.title}":`, error.message);
        errors++;
      }
    }

    console.log(`\nMigration completed:`);
    console.log(`- Successfully migrated: ${migrated} features`);
    console.log(`- Errors: ${errors}`);

    // Clear any deduplication cache/state
    // The new DiscoveredFeature model uses fingerprints for deduplication
    console.log('\nDeduplication will be handled by the new fingerprint system');

    await mongoose.disconnect();
    console.log('Disconnected from MongoDB');
  } catch (error) {
    console.error('Migration error:', error);
    process.exit(1);
  }
}

// Run migration
migrateDiscoveredFeatures();